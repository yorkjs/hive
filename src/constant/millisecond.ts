/**
 * 毫秒数：秒
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 1000
 */
export const MS_SECOND = 1000
/**
 * 毫秒数：分钟
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 60 * MS_SECOND
 */
export const MS_MINUTE = 60 * MS_SECOND
/**
 * 毫秒数：小时
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 60 * MS_MINUTE
 */
export const MS_HOUR = 60 * MS_MINUTE
/**
 * 毫秒数：天
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 24 * MS_HOUR
 */
export const MS_DAY = 24 * MS_HOUR
/**
 * 毫秒数：周
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 7 * MS_DAY
 */
export const MS_WEEK = 7 * MS_DAY
/**
 * 毫秒数：年
 * @type {number}
 * @group Constant
 * @category Millisecond
 * @default 365 * MS_DAY
 */
export const MS_YEAR = 365 * MS_DAY