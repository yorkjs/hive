/**
 * 体积（单位： KB）
 * @type {number}
 * @group Constant
 * @category Size
 * @default 1024
 */
export const SIZE_KB = 1024

/**
 * 体积（单位： MB）
 * @type {number}
 * @group Constant
 * @category Size
 * @default 1024 * SIZE_KB
 */
export const SIZE_MB = 1024 * SIZE_KB

/**
 * 体积（单位： GB）
 * @type {number}
 * @group Constant
 * @category Size
 * @default 1024 * SIZE_MB
 */
export const SIZE_GB = 1024 * SIZE_MB