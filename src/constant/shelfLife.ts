/**
 * 保质期(日)
 * @type {number}
 * @group Constant
 * @category ShelfLife
 * @default 24
 */
export const SHELF_LIFE_DAY = 24

/**
 * 保质期：月
 * @type {number}
 * @group Constant
 * @category ShelfLife
 * @default 30 * SHELF_LIFE_DAY
 */
export const SHELF_LIFE_MONTH = 30 * SHELF_LIFE_DAY

/**
 * 保质期：年
 * @type {number}
 * @group Constant
 * @category ShelfLife
 * @default 365 * SHELF_LIFE_DAY
 */
export const SHELF_LIFE_YEAR = 365 * SHELF_LIFE_DAY